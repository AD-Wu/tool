package com.x.commons.enums;

/**
 * @Desc TODO
 * @Date 2019-11-07 22:05
 * @Author AD
 */
public final class Regex {

    private Regex() {}

    /**
     * 汉字
     */
    public static final String CHINESE = "^[一-龥]";

    /**
     * 汉字，不包含数字、字母、标点
     */
    public static final String ONLY_CHINESE = CHINESE + "+$";

    /**
     * 只有英文，不包含汉字、数字、标点
     */
    public static final String ONLY_ENGLISH = "^[A-Za-z]+$";

    /**
     * 整数，包括Integer、Long
     */
    public static final String LONG = "^(-|\\+)?\\d+$";

    /**
     * 数字
     */
    public static final String NUMERIC = "^(-|\\+)?\\d+(\\.\\d+)?$";

    /**
     * 无符号数字
     */
    public static final String UNSIGNED_NUMERIC = "^\\d+$";

    /**
     * URL
     */
    public static final String URL = "^[a-zA-Z]+:\\/\\/(\\w+(-\\w+)*)(\\.(\\w+(-\\w+)*))*(\\?\\S*)?$";

    /**
     * 邮箱
     */
    public static final String EMAIL = "^[\\w-]+(\\.[\\w-]+)*@[\\w-]+(\\.[\\w-]+)+$";

    /**
     * 浮点数
     */
    public static final String DOUBLE = "^(-?\\d+)(\\.\\d+)?$";

    /**
     * 时间Time
     */
    public static final String TIME = "^((((0?[0-9])|([1][0-9])|([2][0-3]))\\:([0-5]?[0-9])((\\s)|(\\:([0-5]?[0-9])))" +
            "))?$";

    /**
     * 日期Date
     */
    public static final String DATE = "^((\\d{2}(([02468][048])|([13579][26]))[\\-\\/\\s]?((((0?[13578])|(1[02]))" +
            "[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])|(3[01])))|(((0?[469])|(11))[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])|(30))" +
            ")|(0?2[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])))))|(\\d{2}(([02468][1235679])|([13579][01345789]))" +
            "[\\-\\/\\s]?((((0?[13578])|(1[02]))[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])|(3[01])))|(((0?[469])|(11))" +
            "[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])|(30)))|(0?2[\\-\\/\\s]?((0?[1-9])|(1[0-9])|(2[0-8]))))))?$";

    /**
     * 日期和时间
     */
    public static final String DATE_TIME = "^((\\d{2}(([02468][048])|([13579][26]))[\\-\\/\\s]?((((0?[13578])|(1[02])" +
            ")[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])|(3[01])))|(((0?[469])|(11))[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])|(30)" +
            "))|(0?2[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])))))|(\\d{2}(([02468][1235679])|([13579][01345789]))" +
            "[\\-\\/\\s]?((((0?[13578])|(1[02]))[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])|(3[01])))|(((0?[469])|(11))" +
            "[\\-\\/\\s]?((0?[1-9])|([1-2][0-9])|(30)))|(0?2[\\-\\/\\s]?((0?[1-9])|(1[0-9])|(2[0-8]))))))(\\s((" +
            "(0?[0-9])|([1][0-9])|([2][0-3]))\\:([0-5]?[0-9])((\\s)|(\\:([0-5]?[0-9])))))?$";

    /**
     * 日期或时间
     */
    public static final String DATE_OR_TIME = "^(\\d{1,4})[\\-\\/\\s]?(\\d{1,2})[\\-\\/\\s]?(\\d{1,2})([\\s](\\d{1," +
            "2})([:](\\d{1,2})([:](\\d{1,2})([.](\\d{1,3}))?)?)?)?$";

}
